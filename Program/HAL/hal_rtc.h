#ifndef __HAL_RTC_H_
#define __HAL_RTC_H_
/*******************************************头文件区****************************************************/
#include "stm32f10x.h"

/*******************************************配置区******************************************************/
#define DEF_STM32F10X_RTC_EN    1u

/****************************************自定义类型区***************************************************/
typedef struct 
{
    uint16_t year;
    uint8_t  month;
    uint8_t  date;
    uint8_t  week;
    uint8_t  hour;
    uint8_t  min;
    uint8_t  sec;			
}S_GregorianCalendarTimeDef;
    
/*******************************************接口区*******************************************************/
/*********************************************************************************************************
** 函数名称 ：fRTC_Init
** 函数功能 ：RTC初始化
** 入口参数 ：None
** 出口参数 ：成功:0 失败:1
** 作者日期 ：Jason 2020-12-11
**--------------------------------------------------------------------------------------------------------
** 修改作者 ：None
** 修改日期 ：None
** 修改内容 ：None
***********************************************************************************************************/
extern uint8_t fRTC_Init(void);
/*********************************************************************************************************
** 函数名称 ：fRTC_Set
** 函数功能 ：设置RTC时间
** 入口参数 ：year : 年(1970~2099)
**			  mon  : 月
**			  day  : 日
**			  hour : 时
**			  min  : 分
**			  sec  : 秒
** 出口参数 ：成功:0 失败:1
** 作者日期 ：Jason 2020-12-11
**--------------------------------------------------------------------------------------------------------
** 修改作者 ：None
** 修改日期 ：None
** 修改内容 ：None
***********************************************************************************************************/
extern uint8_t fRTC_Set(uint16_t year, uint8_t mon, uint8_t day, uint8_t hour, uint8_t min, uint8_t sec);
/*********************************************************************************************************
** 函数名称 ：RTC_Get_Week
** 函数功能 ：输入公历日期得到星期(只允许1901-2099年)
** 入口参数 ：year : 年(1970~2099)
**			  mon  : 月
**			  day  : 日
** 出口参数 ：星期号
** 作者日期 ：Jason 2020-12-11
**--------------------------------------------------------------------------------------------------------
** 修改作者 ：None
** 修改日期 ：None
** 修改内容 ：None
***********************************************************************************************************/
extern uint8_t fRTC_GetWeek(uint16_t year,uint8_t month,uint8_t day);
/*********************************************************************************************************
** 函数名称 ：fRTC_Get
** 函数功能 ：获取当年公历时间
** 入口参数 ：S_GregorianCalendarTimeDef类型指针,且必须为static。
** 出口参数 ：成功:0 失败:1
** 作者日期 ：Jason 2020-12-11
**--------------------------------------------------------------------------------------------------------
** 修改作者 ：None
** 修改日期 ：None
** 修改内容 ：None
***********************************************************************************************************/
extern uint8_t fRTC_Get(S_GregorianCalendarTimeDef *timer);

#endif
